function [alfa,beta]=tridLU(a,e,c)
% INPUT:
%   _                                  _
%   |a(1) c(1)  0 ..................   0|
%   |e(2) a(2) c(2)  0.................0|
%   | 0   e(3) a(3) c(3) 0.............0|
%   |                                   |
%   |                                   |  = A
%   |                                   |
%   |                                   |
%   |0.......................0 e(n) a(n)|
%   
% OUTPUT A=LU with
%   | 1      0  0 ..................   0|
%   |beta(2) 1 0.......................0|
%   | 0    beta(3) 1  0................0|
%   |                                   |
%   |                                   |  = L
%   |                                   |
%   |                                   |
%   |0.......................0 beta(n) 1|
%   _                                  _
%   |alpha(1) c(1)  0 ..............   0|
%   | 0  alpha(2) c(2)  0..............0|
%   | 0    0 alpha(3) c(3) 0...........0|
%   |                                   |
%   |                                   |  = U
%   |                                   |
%   |0 ..............0 alpha(n-1) c(n-1)|
%   |0........................0 alpha(n)|
%   
n=length(a);
alfa(1)=a(1);
for i=2:n
    beta(i)=e(i)/alfa(i-1);
    alfa(i)=a(i)-beta(i)*c(i-1);
end
